package org.getback4j.getback.main;

/**
 *
 * @author Bryce
 */
public class Config {

    /**
     * Contains if GetBack is in Verbose mode
     */
    public Boolean verbose;

    public Config() {

    }

    /**
     * Checks if GetBack is in Verbose mode
     *
     * @return true/false if GetBack is in Verbose mode
     */
    public Boolean getVerbose() {
        return verbose;
    }

}
